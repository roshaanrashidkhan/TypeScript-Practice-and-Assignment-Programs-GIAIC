var age = 64;
if (age < 2) {
    console.log("You're a Baby!");
}
else if (age >= 2 && age < 4) {
    console.log("You're a Toddler Now!");
}
else if (age >= 4 && age < 13) {
    console.log("You're a Kid Now!");
}
else if (age >= 13 && age < 20) {
    console.log("You're a Teenager Now!");
}
else if (age >= 20 && age < 65) {
    console.log("You're a Adult Now!");
}
else if (age >= 65) {
    console.log("You're an Elder Now!");
}
